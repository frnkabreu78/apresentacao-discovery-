  /* ──────────────────────────────────────────────────────────
     ENGINE
  ────────────────────────────────────────────────────────── */
  function translateNode(node) {
    if (node.nodeType === Node.TEXT_NODE) {
      var t = node.nodeValue;
      if (!t) return;
      var trimmed = t.trim();
      if (!trimmed) return;
      // Exact match
      if (T[trimmed] !== undefined) {
        node.nodeValue = node.nodeValue.replace(trimmed, T[trimmed]);
        return;
      }
      // Substring match for JSX-fragmented nodes (only keys longer than 8 chars)
      var keys = Object.keys(T);
      for (var i = 0; i < keys.length; i++) {
        var k = keys[i];
        if (k.length > 8 && t.indexOf(k) !== -1) {
          t = t.split(k).join(T[k]);
        }
      }
      if (t !== node.nodeValue) node.nodeValue = t;
    }
  }

  function translateTextNodes(root) {
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null, false);
    var node;
    while ((node = walker.nextNode())) {
      translateNode(node);
    }
  }

  function translateAttrs(root) {
    var scope = root && root.querySelectorAll ? root : document;
    scope.querySelectorAll('[placeholder]').forEach(function (el) {
      var v = el.getAttribute('placeholder');
      if (v && TA[v]) el.setAttribute('placeholder', TA[v]);
    });
    scope.querySelectorAll('[aria-label]').forEach(function (el) {
      var v = el.getAttribute('aria-label');
      if (v && TA[v]) el.setAttribute('aria-label', TA[v]);
    });
    scope.querySelectorAll('[alt]').forEach(function (el) {
      var v = el.getAttribute('alt');
      if (v && TA[v]) el.setAttribute('alt', TA[v]);
    });
    // lang selector label
    scope.querySelectorAll('[lang],[lang="pt-BR"]').forEach(function (el) {
      // handled via text nodes
    });
  }

  function applyAll() {
    if (localStorage.getItem('portfolio-lang') !== 'en') return;
    translateTextNodes(document.body);
    translateAttrs(document.body);
  }

  /* MutationObserver — watch React DOM updates */
  var observer = new MutationObserver(function (mutations) {
    if (localStorage.getItem('portfolio-lang') !== 'en') return;
    mutations.forEach(function (m) {
      m.addedNodes.forEach(function (n) {
        if (n.nodeType === Node.ELEMENT_NODE) {
          translateTextNodes(n);
          translateAttrs(n);
        } else if (n.nodeType === Node.TEXT_NODE) {
          translateNode(n);
        }
      });
      // also handle character data changes
      if (m.type === 'characterData' && m.target) {
        translateNode(m.target);
      }
    });
  });

  /* Wait for React to mount .pres-root, then apply translations */
  function waitAndApply() {
    if (localStorage.getItem('portfolio-lang') !== 'en') return;
    var root = document.getElementById('root');
    if (!root) return;
    // Apply immediately on whatever is rendered
    applyAll();
    // Watch for future React updates
    observer.observe(root, { childList: true, subtree: true, characterData: true });
    // Also re-apply after short delays to catch lazy-rendered slides
    setTimeout(applyAll, 500);
    setTimeout(applyAll, 1500);
    setTimeout(applyAll, 3000);
  }

  // Expose for pfSetLang in app.html
  window._applyTranslations = applyAll;
  window._pfTranslationsActive = true;

  document.addEventListener('DOMContentLoaded', waitAndApply);

  // Re-apply when storage fires (pfSetLang dispatches this)
  window.addEventListener('storage', function (e) {
    if (e.key === 'portfolio-lang') {
      if (e.newValue === 'en') {
        applyAll();
      }
    }
  });
